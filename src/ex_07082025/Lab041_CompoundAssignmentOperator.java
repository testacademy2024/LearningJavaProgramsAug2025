package ex_07082025;

public class Lab041_CompoundAssignmentOperator {
    public static void main(String[] args) {
        int a = 10;
        // += , -=, /=, *= . %=
        // += ->
        a+=10; // a = a+10
        a-=10; // a = a-10
        a/=10; // a = a/10
        a*=10; // a = a*10
    }
}
