package ex_18082025;

public class Lab120_ForLoopsOtherLoops {
    public static void main(String[] args) {
        // Loop with condition
        //
        for(int i=0;i<10;i++)
        {
            System.out.println(i);
            if(i==5)
            {
break;
            }
        }
        // i = 1 -> 1
        // i = 2 -> 2
        // i = 3 -> 3
        // i = 4 -> 4
        // i = 5 -> Out of loop

    }
}
