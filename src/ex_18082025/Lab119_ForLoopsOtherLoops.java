package ex_18082025;

public class Lab119_ForLoopsOtherLoops {
    public static void main(String[] args) {
        // Loop with condition
        //
        for(int i=0;i<=10;i++)
        {
            // i from 1 to 10, 10 times
            if(i==5)
            {
                System.out.println("Number is equal to "+i);
            }
            else {
                System.out.println(i);
            }
        }
    }
}
