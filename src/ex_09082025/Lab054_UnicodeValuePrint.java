package ex_09082025;

public class Lab054_UnicodeValuePrint {
    public static void main(String[] args) {
        byte a=10;
        byte b=20;
        int c=a+b; // as per operand rules
        System.out.println(c);
        char a1='A';
        char b1='B';
        int c1=a1+b1;
        System.out.println(c1);
        System.out.println('A' == 65);
        System.out.println('B' == 69);
    }
}
