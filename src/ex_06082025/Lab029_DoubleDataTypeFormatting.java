package ex_06082025;

public class Lab029_DoubleDataTypeFormatting {
    public static void main(String[] args) {
        double a=10;
        double b=34;
        double sum=a+b;
        double sub=a-b;
        double mul=a*b;
        double div=a/b;
        System.out.printf("The sum is : %f\n",sum);
        System.out.printf("The sub is : %f\n",sub);
        System.out.printf("The multiplication is : %f\n",mul);
        System.out.printf("The division is: %f\n",div);
    }
}
